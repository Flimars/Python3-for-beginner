# 4. Desenvolva um algoritmo para calcular a área de um triângulo. Pensem nas
# variáveis que serão necessárias. Ao final, o algoritmo deve informar a área total do
# triângulo.

b = int(input('Informe a medida da base do triânfulo: '))
h = int(input('Informe a medida da altura triângulo: '))
area = (b*h)/2
print('O valor da área do triângulo é: ', area)
